# Crear una lista
frutas = ['manzana', 'plátano', 'cereza', 'naranja']

# Acceder a elementos de la lista (por índice)
print(frutas[0])  # manzana
print(frutas[2])  # cereza

# Cambiar un valor en la lista
frutas[1] = 'kiwi'

# Agregar un nuevo elemento al final de la lista
frutas.append('mango')

# Eliminar un elemento de la lista
frutas.remove('naranja')

# Imprimir la lista modificada
print(frutas)  # ['manzana', 'kiwi', 'cereza', 'mango']
